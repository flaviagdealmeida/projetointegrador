
public class Numero2 {

	public static void main(String[] args) {
		 int numeros[]=new int [20];
		 
		 for (int i = 0; i<20;i++){
			 numeros[i]=i+1;
			 
		 }
				 
		 for (int i = 0; i<20;i++){
			 
			 if (numeros[i]%2==0)
				 System.out.println(numeros[i]);
			 
		 }
		
	}

}
